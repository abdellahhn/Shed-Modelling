    private void drawingPanelMouseDragged(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_drawingPanelMouseDragged
        // TODO add your handling code here:
        ValeurImperiale upX = controller.resecale(evt.getX(), this.echelle, this.offsetX);
        ValeurImperiale upY = controller.resecale(evt.getY(), this.echelle, this.offsetY);

        ValeurImperiale impPxlX = new ValeurImperiale(upX.getValeurEntiere());
        ValeurImperiale impPxlY = new ValeurImperiale(upY.getValeurEntiere());
        Position newPos = new Position(impPxlX, impPxlY);

        if (controller.isAccessoryFound()) {
            String accessoryType = controller.getFoundAccessoryType();
            Position oldPos = controller.getSelectedAccessoryPosition();

            if ("Window".equals(accessoryType)) {
                controller.moveWindow(oldPos, newPos, getVoirFacade());
            } else if ("Door".equals(accessoryType)) {
                controller.moveDoor(oldPos, newPos, getVoirFacade());
            }
        }

        this.drawingPanel.repaint();

    }//GEN-LAST:event_drawingPanelMouseDragged

