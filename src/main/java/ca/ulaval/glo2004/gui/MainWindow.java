    private void drawingPanelMouseMoved(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_drawingPanelMouseMoved
        // TODO add your handling code here:
        ValeurImperiale upX = controller.resecale(evt.getX(), this.echelle, this.offsetX);
        ValeurImperiale upY = controller.resecale(evt.getY(), this.echelle, this.offsetY);

        ValeurImperiale impPxlX = new ValeurImperiale(upX.getValeurEntiere());
        ValeurImperiale impPxlY = new ValeurImperiale(upY.getValeurEntiere());

        Position pos = new Position(impPxlX, impPxlY);

        if (!Objects.equals(this.objectToPaint, "VueDessus")) {
            String facade = controller.getFacadeWallMouse(pos, this.getObjectToPaint());
            String height = controller.getHeightWallMouse(pos, this.getObjectToPaint());
            String width = controller.getWidthWallMouse(pos, this.getObjectToPaint());
            String depth = controller.getDepthWallMouse(pos, this.getObjectToPaint());

            if (!facade.equals("Not Found") || !height.equals("Not Found") ||
                    !width.equals("Not Found") || !depth.equals("Not Found")) {

                this.msgText.setText("Facade: " + facade + "\n");
                this.msgText.append("Height: " + height + "\n");
                this.msgText.append("Width: " + width + "\n");
                this.msgText.append("Depth: " + depth + "\n");

                lastDisplayedFacade = facade;
                lastDisplayedHeight = height;
                lastDisplayedWidth = width;
                lastDisplayedDepth = depth;
            } else {
                this.msgText.setText("");
                lastDisplayedFacade = "";
                lastDisplayedHeight = "";
                lastDisplayedWidth = "";
                lastDisplayedDepth = "";
            }
        }

        this.drawingPanel.repaint();
    }//GEN-LAST:event_drawingPanelMouseMoved

