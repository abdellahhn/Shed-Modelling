    private int convertTextDimInt(String number) {
        if (number.isEmpty()) {
            return 10;
        }
        String[] args = number.split(" ");
        int res = Integer.parseInt(args[0]);

        if (args.length > 1) {
            String[] fraction = args[1].split("/");
            int i = Integer.parseInt(fraction[0]);
            int j = Integer.parseInt(fraction[1]);
            res += i / j;
        }

        return res;
    }

private double convertTextToDouble(String number) {
    return Double.parseDouble(number);
}

